var docs = {};

docs.isMac = false;
let macPlatforms = ["MacIntel", "MacPPC", "Mac68K", "iPhone", "iPad"];
if (macPlatforms.includes(navigator.platform)) {
	docs.isMac = true;
}

// Helper method to translate keys (including a few special/command keys) to
// keyCodes for use with docs.pressKey(keyCode, ...).
if (!docs.isMac) {
	docs.codeFromKey = function (key) {
		const specialKeys = {
			Backspace: 8,
			Tab: 9,
			Enter: 13,
			Shift: 16,
			Control: 17,
			Escape: 27,
			Space: 32,
			End: 35,
			Home: 36,
			ArrowLeft: 37,
			ArrowUp: 38,
			ArrowRight: 39,
			ArrowDown: 40,
			Delete: 46,
			Z: 90,
			PageUp: 33,
			PageDown: 34,
			f: 70,
			F: 70,
		};
		if (key in specialKeys) {
			return specialKeys[key];
		}
		return key.charCodeAt(key);
	};
} else {
	docs.codeFromKey = function (key) {
		const specialKeys = {
			Backspace: 8,
			Tab: 9,
			Enter: 13,
			Shift: 16,
			Control: 18, // Critical
			Escape: 27,
			End: 35,
			Home: 36,
			ArrowLeft: 37,
			ArrowUp: 38,
			ArrowRight: 39,
			ArrowDown: 40,
			Delete: 46,
			Z: 90,
            V: 86, // Critical
			PageUp: 33,
			PageDown: 34,
			f: 70,
			F: 70,
		};
		if (key in specialKeys) {
			return specialKeys[key];
		}
		return key.charCodeAt(key);
	};
}

