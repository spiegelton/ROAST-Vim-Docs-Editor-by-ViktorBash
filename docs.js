// Helper method to observe changes to a DOM element. Used in the getSelection
// and getCurrentParagraph methods, where we watch the element that Kix puts
// the selection into when you press ctrl+C.
docs.observe = function (el, config, callback, observeOnce) {
	observeOnce = typeof observeOnce !== "undefined" && observeOnce;

	var observer = new MutationObserver(function (mutations) {
		if (observeOnce) {
			observer.disconnect();
		}

		callback(mutations);
	});

	observer.observe(el, config);
	return observer;
};

docs.isMac = false;
let macPlatforms = ["MacIntel", "MacPPC", "Mac68K"];
if (macPlatforms.includes(navigator.platform)) {
	docs.isMac = true;
}

// Helper method to translate keys (including a few special/command keys) to
// keyCodes for use with docs.pressKey(keyCode, ...).
if (!docs.isMac) {
	docs.codeFromKey = function (key) {
		const specialKeys = {
			Backspace: 8,
			Tab: 9,
			Enter: 13,
			Shift: 16,
			Control: 17,
			Escape: 27,
			End: 35,
			Home: 36,
			ArrowLeft: 37,
			ArrowUp: 38,
			ArrowRight: 39,
			ArrowDown: 40,
			Delete: 46,
			Z: 90,
		};
		if (key in specialKeys) {
			return specialKeys[key];
		}
		return key.charCodeAt(key);
	};
} else {
	docs.codeFromKey = function (key) {
		const specialKeys = {
			Backspace: 8,
			Tab: 9,
			Enter: 13,
			Shift: 16,
			Control: 18, // Critical
			Escape: 27,
			End: 35,
			Home: 36,
			ArrowLeft: 37,
			ArrowUp: 38,
			ArrowRight: 39,
			ArrowDown: 40,
			Delete: 46,
			Z: 90,
            V: 86, // Critical
		};
		if (key in specialKeys) {
			return specialKeys[key];
		}
		return key.charCodeAt(key);
	};
}

