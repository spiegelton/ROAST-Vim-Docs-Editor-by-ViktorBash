// texttarget is the underlying text editor that Docs uses for catching keyboard input, etc

// We use this for copying text only (basically just for y commands)
docs.contentDocument = document.querySelector(
	".docs-texteventtarget-iframe"
).contentDocument;

// This is very important, and is the place where we direct all of our key events
docs.texttarget = docs.contentDocument.querySelector(
	'[contenteditable="true"]'
);

// You can catch user input in the editor by setting docs.keydown to be your
// handler. Key presses generated by Docs+ will be ignored. You should be able
// to use e.key in all modern browsers.
docs.keydown_ = function (e) {
	if (e.docs_plus_ || !docs.keydown) {
		return;
	}
	return docs.keydown(e);
};
docs.texttarget.addEventListener("keydown", docs.keydown_);

// Simulate a key press.
// keyCode, ctrlKey (or altKey on Mac), shiftKey, commandKey(Mac only)
docs.pressKey = function (keyCode, ctrlKey, shiftKey) {
	var el = document.getElementsByClassName("docs-texteventtarget-iframe")[0];
	el = el.contentDocument;

	let is_command = keyCode <= 46 || ctrlKey;

	let data;
	if (!docs.isMac) {
		data = { keyCode: keyCode, ctrlKey: ctrlKey, shiftKey: shiftKey };
	} else {
		data = { keyCode: keyCode, altKey: ctrlKey, shiftKey: shiftKey };

		// Now we have to check for Mac-Specific modifications that may need to be done
		if (keyCode === docs.codeFromKey("Z") && ctrlKey === true) {
			// Undo
			data = { keyCode: keyCode, metaKey: true };
		} else if (keyCode === docs.codeFromKey("Home") && ctrlKey === true) {
			// Home  (to go up)
			data = {
				keyCode: docs.codeFromKey("ArrowUp"),
				shiftKey: shiftKey,
				metaKey: true,
			};
		} else if (keyCode === docs.codeFromKey("End") && ctrlKey === true) {
			// End (to go down)
			data = {
				keyCode: docs.codeFromKey("ArrowDown"),
				shiftKey: shiftKey,
				metaKey: true,
			};
		} 
		else if (keyCode === docs.codeFromKey("Y") && ctrlKey === true) {
			data = {
				keyCode: keyCode,
				metaKey: true
			}
		}
		else if (keyCode === docs.codeFromKey("f") && ctrlKey === true) {
			// Works for both "F" and "f" since they both have same keycode
			data = {
				keyCode: keyCode,
				metaKey: true
			}
		}
		
		else if (keyCode === docs.codeFromKey("V")) {
            // Paste
			// TODO: Remove this (appears unused in reality)
            data = { keyCode: keyCode, metaKey: true };
		}
	}

	var key_event;
	if (is_command) {
		key_event = new KeyboardEvent("keydown", data);
	} else {
		key_event = new KeyboardEvent("keypress", data);
	}
	key_event.docs_plus_ = true;

	el.dispatchEvent(key_event);
};

docs.pressSpecialKey = function (key) {
	var el = document.getElementsByClassName("docs-texteventtarget-iframe")[0];
	el = el.contentDocument;
	let data = { key: key };
	let isCommand = false;
	switch (key) {
		case "$":
			data.keyCode = 36;
			data.shiftKey = true;
			data.code = "Digit4";
			break;
		case " ":
			data.keyCode = 32;
			data.code = "Space";
			break;
		case "Tab":
			data.keyCode = 9;
			data.code = "Tab";
			isCommand = true;
			break;
		case "#":
			data.keyCode = 35;
			data.shiftKey = true;
			data.code = "Digit3";
			break;
		case "%":
			data.keyCode = 37;
			data.shiftKey = true;
			data.code = "Digit5";
			break;
		case "&":
			data.keyCode = 38;
			data.shiftKey = true;
			data.code = "Digit7";
			break;
		case "*":
			data.keyCode = 42;
			data.shiftKey = true;
			data.code = "Digit8";
			break;
		case "(":
			data.keyCode = 40;
			data.shiftKey = true;
			data.code = "Digit9";
			break;
		case ")":
			data.keyCode = 41;
			data.shiftKey = true;
			data.code = "Digit0";
			break;
		case "+":
			data.keyCode = 43;
			data.shiftKey = true;
			data.code = "Equal";
			break;
		case "-":
			data.keyCode = 45;
			data.code = "Minus";
			break;
		case "!":
			data.keyCode = 33;
			data.shiftKey = true;
			data.code = "Digit1";
			break;
		case ",":
			data.keyCode = 44;
			data.code = "Comma";
			break;
		case ".":
			data.keyCode = 46;
			data.code = "Period";
			break;
		case "'":
			data.keyCode = 39;
			data.code = "Quote";
			break;
		case "\"":
			data.keyCode = 34;
			data.shiftKey = true;
			data.code = "Quote";
			break;
	}

	let key_event;
	if (!isCommand) {
		key_event = new KeyboardEvent("keypress", data);
	}
	else {
		key_event = new KeyboardEvent("keydown", data);
	}
	key_event.docs_plus_ = true;

	el.dispatchEvent(key_event);
}

// Pastes plain text into the document
docs.pasteClipboardPlainText = async function () {
	let data = new DataTransfer();
	data.setData("text/plain", await navigator.clipboard.readText());
	var paste = new ClipboardEvent("paste", {
		clipboardData: data,
		dataType: "text",
	});
	paste.docs_plus_ = true;

	await docs.texttarget.dispatchEvent(paste);
};

