// You can catch user input in the editor by setting docs.keydown to be your
// handler. Key presses generated by Docs+ will be ignored. You should be able
// to use e.key in all modern browsers.
docs.keydown_ = function (e) {
    console.log(e);
	if (e.docs_plus_ || !docs.keydown) {
		return;
	}
	return docs.keydown(e);
};
docs.texttarget.addEventListener("keydown", docs.keydown_);

// Simulate a key press.
// keyCode, ctrlKey (or altKey on Mac), shiftKey, commandKey(Mac only)
docs.pressKey = function (keyCode, ctrlKey, shiftKey) {
	var el = document.getElementsByClassName("docs-texteventtarget-iframe")[0];
	el = el.contentDocument;

	let is_command = keyCode <= 46 || ctrlKey;

	let data;
	if (!docs.isMac) {
		data = { keyCode: keyCode, ctrlKey: ctrlKey, shiftKey: shiftKey };
	} else {
		data = { keyCode: keyCode, altKey: ctrlKey, shiftKey: shiftKey };

		// Now we have to check for Mac-Specific modifications that may need to be done
		if (keyCode === docs.codeFromKey("Z") && ctrlKey === true) {
			// Undo
			data = { keyCode: keyCode, metaKey: true };
		} else if (keyCode === docs.codeFromKey("Home") && ctrlKey === true) {
			// Home  (to go up)
			data = {
				keyCode: docs.codeFromKey("ArrowUp"),
				shiftKey: shiftKey,
				metaKey: true,
			};
		} else if (keyCode === docs.codeFromKey("End") && ctrlKey === true) {
			// End (to go down)
			data = {
				keyCode: docs.codeFromKey("ArrowDown"),
				shiftKey: shiftKey,
				metaKey: true,
			};
		} else if (keyCode === docs.codeFromKey("V")) {
            // Paste
            data = { keyCode: keyCode, metaKey: true };
            console.log(data);
		}
	}

	var key_event;
	if (is_command) {
		key_event = new KeyboardEvent("keydown", data);
	} else {
		key_event = new KeyboardEvent("keypress", data);
	}
	key_event.docs_plus_ = true;

	el.dispatchEvent(key_event);
};

// Pastes a block of plaintext into the document. Note that spaces before/after
// @text don't seem to work. Also note that repeated pastes can cause serious
// problems --- please give each paste a few hunderd milliseconds to finish
// before sending another pasteText.
docs.pasteClipboard = async function () {
	let data = new DataTransfer();
	data.setData("text/plain", await navigator.clipboard.readText());
	var paste = new ClipboardEvent("paste", {
		clipboardData: data,
		dataType: "text",
	});
	paste.docs_plus_ = true;

	await docs.texttarget.dispatchEvent(paste);
    return;
};

